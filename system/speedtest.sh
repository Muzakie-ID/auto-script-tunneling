#!/bin/bash

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
NC='\033[0m'

clear
echo -e "${CYAN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo -e "${GREEN}               SPEEDTEST                          ${NC}"
echo -e "${CYAN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo ""

# Check if speedtest-cli is installed
if ! command -v speedtest-cli &> /dev/null; then
    echo -e "${YELLOW}speedtest-cli not found. Installing...${NC}"
    apt-get update > /dev/null 2>&1
    apt-get install -y speedtest-cli > /dev/null 2>&1
    echo -e "${GREEN}✓ speedtest-cli installed${NC}"
    echo ""
fi

echo -e "${YELLOW}Running speedtest... Please wait...${NC}"
echo ""

# Run speedtest
speedtest-cli --simple

echo ""
echo -e "${CYAN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo -e "${GREEN}Speedtest completed!${NC}"
echo -e "${CYAN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo ""
read -p "Press [Enter] to continue..."
/usr/local/sbin/tunneling/menu/system-menu.sh
