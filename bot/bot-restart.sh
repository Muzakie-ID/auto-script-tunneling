#!/bin/bash

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
NC='\033[0m'

clear
echo -e "${CYAN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo -e "${GREEN}            RESTART BOT TELEGRAM                   ${NC}"
echo -e "${CYAN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo ""

echo -e "${YELLOW}Restarting bot...${NC}"
systemctl restart telegram-bot
sleep 2

if systemctl is-active --quiet telegram-bot; then
    echo -e "${GREEN}✓ Bot restarted successfully!${NC}"
else
    echo -e "${RED}✗ Failed to restart bot${NC}"
    echo ""
    echo -e "${YELLOW}Error logs:${NC}"
    journalctl -u telegram-bot -n 20 --no-pager
fi

echo ""
echo -e "${CYAN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo ""
read -p "Press [Enter] to continue..."
/usr/local/sbin/tunneling/bot-menu.sh
